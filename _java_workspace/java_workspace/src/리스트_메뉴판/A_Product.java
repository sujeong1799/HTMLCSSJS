package 리스트_메뉴판;

import java.util.ArrayList;

public class A_Product {

	private String menu;
	private int price;
	private int count;
	
	public A_Product() {}

	public A_Product(String menu, int price) {
		this.menu = menu;
		this.price = price;
	}

	public String getMenu() {
		return menu;
	}

	public void setMenu(String menu) {
		this.menu = menu;
	}

	public int getPrice() {
		return price;
	}

	public void setPrice(int price) {
		this.price = price;
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}

	
	
	
	
	
	
	
}
